    echo -e "${GREEN}✓ Objective '$objective' created successfully${NC}"
    echo -e "  ${CYAN}Location: $objective_dir${NC}"
    echo -e "  ${CYAN}Agents: 5 (a1-a5)${NC}"
    echo -e "  ${CYAN}Goals per agent: 5 (g1-g5)${NC}"
    echo -e "  ${CYAN}Total goals: 25${NC}"
}

# Generate master plan
plan_objective() {
    local objective="$1"
    
    if [[ -z "$objective" ]]; then
        echo -e "${RED}Error: Objective name required${NC}"
        echo "Usage: $0 plan <objective>"
        exit 1
    fi
    
    local objective_dir="$CHECKLIST_BASE/$objective"
    if [[ ! -d "$objective_dir" ]]; then
        echo -e "${RED}Error: Objective '$objective' not found${NC}"
        exit 1
    fi
    
    echo -e "${CYAN}Generating master plan for: $objective${NC}"
    local plan_file="$objective_dir/plan.md"
    
    echo "# Master Plan: $objective" > "$plan_file"
    echo -e "\n## Timeline" >> "$plan_file"
    echo "| Goal | Agent | Component | Priority | Duration | Status |" >> "$plan_file"
    echo "|------|-------|-----------|----------|----------|--------|" >> "$plan_file"
    
    local total_duration=0
    for agent in "${AGENTS[@]}"; do
        for goal in "${GOALS[@]}"; do
            local roadmap_file="$objective_dir/$agent/$goal/roadmap.md"
            if [[ -f "$roadmap_file" ]]; then
                local component=$(grep "Component" "$roadmap_file" | sed 's/.*: //')
                local priority=$(grep "Priority" "$roadmap_file" | sed 's/.*: //')
                local duration=$(grep "Estimated Duration" "$roadmap_file" | sed 's/.*: //')
                local status=$(grep -A1 "Status" "$roadmap_file" | grep -E "\[[x ]\]" | sed 's/.*\[\([x ]\)\].*/\1/' | { [[ $(cat) == "x" ]] && echo "Started" || echo "Not Started"; })
                echo "| $goal | $agent | $component | $priority | $duration | $status |" >> "$plan_file"
                [[ "$duration" =~ ([0-9]+) ]] && total_duration=$((total_duration + ${BASH_REMATCH[1]}))
            fi
        done
    done
    
    echo -e "\n## Dependency Graph" >> "$plan_file"
    echo "[Dependencies to be mapped by Throne]" >> "$plan_file"
    
    echo -e "\n## Worker Assignments" >> "$plan_file"
    echo "[Assignments to be updated by Throne via syspulse throne squires assign]" >> "$plan_file"
    
    echo -e "${GREEN}✓ Master plan generated at $plan_file${NC}"
    echo "$(date): Generated master plan for $objective" >> "$objective_dir/master.log"
}

# List objectives, status, and blockers (unchanged from previous, included for completeness)
list_objectives() {
    echo -e "${CYAN}Available Objectives:${NC}"
    echo -e "${CYAN}=====================${NC}"
    
    if [[ ! -d "$CHECKLIST_BASE" ]]; then
        echo -e "${YELLOW}No objectives found. Use 'create' to add one.${NC}"
        return
    fi
    
    local count=0
    for objective_dir in "$CHECKLIST_BASE"/*; do
        if [[ -d "$objective_dir" ]]; then
            local objective=$(basename "$objective_dir")
            echo -e "  ${GREEN}$objective${NC}"
            
            local total_goals=0
            local completed_goals=0
            
            for agent in "${AGENTS[@]}"; do
                for goal in "${GOALS[@]}"; do
                    local roadmap_file="$objective_dir/$agent/$goal/roadmap.md"
                    if [[ -f "$roadmap_file" ]]; then
                        total_goals=$((total_goals + 1))
                        if grep -q "Status: .*Completed" "$roadmap_file" 2>/dev/null; then
                            completed_goals=$((completed_goals + 1))
                        fi
                    fi
                done
            done
            
            echo -e "    Progress: $completed_goals/$total_goals goals completed"
            count=$((count + 1))
        fi
    done
    
    if [[ $count -eq 0 ]]; then
        echo -e "${YELLOW}No objectives found.${NC}"
    fi
}

show_status() {
    local objective="$1"
    
    if [[ -z "$objective" ]]; then
        echo -e "${RED}Error: Objective name required${NC}"
        echo "Usage: $0 status <objective>"
        exit 1
    fi
    
    local objective_dir="$CHECKLIST_BASE/$objective"
    
    if [[ ! -d "$objective_dir" ]]; then
        echo -e "${RED}Error: Objective '$objective' not found${NC}"
        exit 1
    fi
    
    echo -e "${CYAN}Objective Status: $objective${NC}"
    echo -e "${CYAN}===============================${NC}"
    echo
    
    for agent in "${AGENTS[@]}"; do
        echo -e "${GREEN}Agent $agent:${NC}"
        
        for goal in "${GOALS[@]}"; do
            local roadmap_file="$objective_dir/$agent/$goal/roadmap.md"
            local status="Not Started"
            local color="${RED}"
            
            if [[ -f "$roadmap_file" ]]; then
                if grep -q "Status: .*Completed" "$roadmap_file" 2>/dev/null; then
                    status="Completed"
                    color="${GREEN}"
                elif grep -q "Status: .*In Progress" "$roadmap_file" 2>/dev/null; then
                    status="In Progress"
                    color="${YELLOW}"
                elif grep -q "Status: .*Blocked" "$roadmap_file" 2>/dev/null; then
                    status="Blocked"
                    color="${RED}"
                fi
            fi
            
            echo -e "  Goal $goal: ${color}$status${NC}"
        done
        echo
    done
}

list_blockers() {
    local objective="$1"
    
    if [[ -z "$objective" ]]; then
        echo -e "${RED}Error: Objective name required${NC}"
        echo "Usage: $0 blockers <objective>"
        exit 1
    fi
    
    local objective_dir="$CHECKLIST_BASE/$objective"
    
    if [[ ! -d "$objective_dir" ]]; then
        echo -e "${RED}Error: Objective '$objective' not found${NC}"
        exit 1
    fi
    
    echo -e "${CYAN}Blockers for Objective: $objective${NC}"
    echo -e "${CYAN}===============================${NC}"
    echo
    
    local has_blockers=0
    for agent in "${AGENTS[@]}"; do
        for goal in "${GOALS[@]}"; do
            local roadmap_file="$objective_dir/$agent/$goal/roadmap.md"
            if [[ -f "$roadmap_file" ]] && grep -q "Status: .*Blocked" "$roadmap_file"; then
                has_blockers=1
                echo -e "${RED}Agent $agent, Goal $goal: Blocked${NC}"
                grep "Blockers:" "$roadmap_file" | sed 's/Blockers:/    Blockers:/'
                grep "Notes:" "$roadmap_file" | sed 's/Notes:/    Notes:/'
                echo
            fi
        done
    done
    
    if [[ $has_blockers -eq 0 ]]; then
        echo -e "${GREEN}No blockers found.${NC}"
    fi
}

# Main command handling
case "${1:-}" in
    "create")
        create_objective "$2"
        ;;
    "list")
        list_objectives
        ;;
    "status")
        show_status "$2"
        ;;
    "blockers")
        list_blockers "$2"
        ;;
    "plan")
        plan_objective "$2"
        ;;
    "help"|"--help"|"-h")
        show_usage
        ;;
    "")
        show_usage
        ;;
    *)
        echo -e "${RED}Error: Unknown command '$1'${NC}"
        echo
        show_usage
        exit 1
        ;;
esac